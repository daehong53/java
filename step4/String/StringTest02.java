public class StringTest02 {

	public static void main(String[] args) {
		
		// "철수야 안녕?" 라고 영희가 말했다.
		// escape char
		String str = "\"철수야 안녕?\" 라고 영희가 말했다.";
		System.out.println(str);
		
		// "그건 C:\Program Files\Java 안에 있어" 라고 철수가 말했다.
		String str2 = "\"그건 C:\\Program Files\\Java 안에 있어\" 라고 철수가 말했다.";
		System.out.println(str2);
		

	}

}
