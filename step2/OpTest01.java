public class OpTest01 {

	public static void main(String[] args) {
		
		int num = 3;
		System.out.println(num++); // 3
		System.out.println(num	); // 4
		System.out.println(++num); // 5
		System.out.println(num	); // 5
		
		int num2 = 3;
		System.out.println(num2--);
		System.out.println(num2);
		
		boolean myBool = true;
		System.out.println(myBool);
		System.out.println(!myBool);

	}

}
